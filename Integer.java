/*
 Create a class with one integer instace variable.
 Initialize the variable using default constructor and parameterized constructor.
 */
import java.util.*;
public class Integer
{
    int x;
    //default constructor or non-parameterized constructor.
    public Integer()
    {
        x = 0;
    }
    //parameterized constructor.
    public Integer(int x1)
    {
        int x = x1;
    }
}