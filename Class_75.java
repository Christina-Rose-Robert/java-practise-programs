//isUpperCase()
public class Class_75
{
    public static void main()
    {
        char ch1 = 'a';
        char ch2 = 'A';
        char ch3 = '@';
        if(Character.isUpperCase(ch1))
        {
            System.out.println(ch1+" is in upper-case.");
        }
        else if(Character.isUpperCase(ch2))
        {
            System.out.println(ch2+" is in upper-case.");
        }
        else if(Character.isUpperCase(ch3))
        {
            System.out.println(ch3+" is in upper-case.");
        }
    }
}